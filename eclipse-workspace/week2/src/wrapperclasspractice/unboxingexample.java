package wrapperclasspractice;
//PROGRAM FOR UNBOXING-CONVRETING WRAPPER CLASS INTO PRIMITIVE DATATYPE
public class unboxingexample {

	public static void main(String[] args) {
//INTEGERE UUBOXING
		Integer a=20;
		 int c=a;
		 System.out.println("integer is"+c);
		 //CHARACTER UNBOXING
		 Character z='a';
		  char b=z;
		  System.out.println("character is"+b);
	}

}
